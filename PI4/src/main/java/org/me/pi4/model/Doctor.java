package org.me.pi4.model;

/**
 * @author 2016203311
 */
public class Doctor{
    
    private int docId;
    private int docCod;
    private String docName;

    public int getDocId() {
        return docId;
    }

    public void setDocId(int docId) {
        this.docId = docId;
    }

    public int getDocCod() {
        return docCod;
    }

    public void setDocCod(int docCod) {
        this.docCod = docCod;
    }

    public String getDocName() {
        return docName;
    }

    public void setDocName(String docName) {
        this.docName = docName;
    }
    
   
}
